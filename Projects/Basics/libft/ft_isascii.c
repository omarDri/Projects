int ft_isascii(char *str)
{
    int c;

    c = 0;
    if (str[c] == '\0')
        return(1);
    while (str[c] != '\0')
    {
        if (str[c] < 0 || str[c] > 127)
            return(0);
        c++;
    }
    return(1);
}