#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*str;
	int		i;

	str = (char *) malloc((ft_strlen(src) + 1));
	i = 0;
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
